/**
 * Recipe_manager View class
 */
class Recipe_managerViewRecipe_manager extends JViewLegacy
{
	/**
	 * View display method
	 * @return void
	 */
	function display($tpl = null)
	{
		// Assign data to the view
		$this->icons			= $this->get('Icons');
		$this->contributors		= Recipe_managerHelper::getContributors();
		
		// get the manifest details of the component
		$this->manifest = Recipe_managerHelper::manifest();
		
		// Set the toolbar
		$this->addToolBar();
		
		// Check for errors.
		if (count($errors = $this->get('Errors')))
		{
			throw new Exception(implode("\n", $errors), 500);
		}

		// Display the template
		parent::display($tpl);

		// Set the document
		$this->setDocument();
	}

	/**
	 * Setting the toolbar
	 */
	protected function addToolBar()
	{
		$canDo = Recipe_managerHelper::getActions('recipe_manager');
		JToolBarHelper::title(JText::_('COM_RECIPE_MANAGER_DASHBOARD'), 'grid-2');

		// set help url for this view if found
		$help_url = Recipe_managerHelper::getHelpUrl('recipe_manager');
		if (Recipe_managerHelper::checkString($help_url))
		{
			JToolbarHelper::help('COM_RECIPE_MANAGER_HELP_MANAGER', false, $help_url);
		}

		if ($canDo->get('core.admin') || $canDo->get('core.options'))
		{
			JToolBarHelper::preferences('com_recipe_manager');
		}
	}

	/**
	 * Method to set up the document properties
	 *
	 * @return void
	 */
	protected function setDocument()
	{
		$document = JFactory::getDocument();
		
		// add dashboard style sheets
		$document->addStyleSheet(JURI::root() . "administrator/components/com_recipe_manager/assets/css/dashboard.css");
		
		// set page title
		$document->setTitle(JText::_('COM_RECIPE_MANAGER_DASHBOARD'));
		
		// add manifest to page JavaScript
		$document->addScriptDeclaration("var manifest = jQuery.parseJSON('" . json_encode($this->manifest) . "');", "text/javascript");
	}
}
